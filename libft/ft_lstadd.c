#include "libft.h"

void	ft_lstadd(t_list **alst, t_list *new)
{
	t_list	*t;

	if (*alst)
	{
		t = new;
		t->next = (*alst);
		(*alst) = t;
	}
	else
		(*alst) = new;
}
